// SWITCH 

let nota = 5
switch(nota){
    case 5: console.log("Has obtenido un 5")
    break
    case 4: console.log("Has obtenido un 4")
    break
    case 3: console.log("Has obtenido un 3")
    break
    case 2: console.log("Has obtenido un 2")
    break
    case 1: console.log("Has obtenido un 1")
    break
    default: console.log("Error")
    break
}
    
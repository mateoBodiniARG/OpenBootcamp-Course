const nota = 5

if (nota === 5){
    console.log("Te has sacado un 5")
} else if(nota === 4){
    console.log("Te has sacado un 4")
} else if(nota === 3){
    console.log("Te has sacado un 3")
} else if(nota === 2){
    console.log("Te has sacado un 2")
} else if(nota === 1){
    console.log("Te has sacado un 1")
}else{
    console.log("Error")
}